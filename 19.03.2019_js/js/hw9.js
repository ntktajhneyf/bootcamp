class Stopwatch {
  constructor(elem) {
    this.elem = elem;
    this.button = null;
    this.startTime = null;
    this.deltaTime = null;
    this.intervalFunction = null;
  }





  get createTimer() {
    this.elem.innerHTML = `<div class="stopwatch">
    <p class="time js-time">00:00.0</p>
    <button class="btn js-start">Start</button>
    <button class="btn js-take-lap">Lap</button>
    <button class="btn js-reset">Reset</button>
  </div>
  <ul class="laps js-laps"></ul>`;

  this.button = document.querySelector('.js-start')
  }


  get toogleAttr() {
    if (elem.dataset.action === 'true') {
      elem.dataset.action = !elem.dataset.action
    } else {
      elem.setAtribute("data-action", true);
    }
    elem.innerHTML = elem.dataset.action === 'true' ? "Pause" : "continue";
  }

  get myTimer() {
    if (!this.intervalFunction) {
      this.intervalFunction = setInterval(function () {
        this.deltaTime = (Date.now() - this.startTime);
      })
    }
  }

  get pauseMyTimer() {
    if (this.intervalFunction) {
      clearInterval(this.intervalFunction);
    }
    this.intervalFunction = null;
  }

  get startTimer() {
    let startBtn = document.querySelector('.js-start')
    this.startTime === null ? this.startTime = Date.now() : this.startTime = (Date.now() - this.deltaTime);

    toogleAttr(startBtn);
    startBtn.dataset.action === 'true' ? this.myTimer() : this.pauseMyTimer();
  }
}
